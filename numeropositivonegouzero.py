# Exercicio de IF ELIF e ELSE
# Desenvolvendo algoritmos em Python

a = input ("Digite um número")
a = int(a)

if(a > 0):
	print("Número Positivo")
	
elif(a == 0):
	print("Zero")
    
elif(a < 0):
    print("Negativo")
    
else:
    print("Digite um numero valido")
    
	
input()
